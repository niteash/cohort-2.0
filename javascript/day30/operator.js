//Aritmetic Operator
// // + - * / ** %

//Assginment Operator
// = += -= *= /= %=

let a = 3;

console.log(a / 3);
console.log(a * 3);
console.log(a + 3);
console.log(a ** 3);
console.log(a % 3);

//Assginment Operator
console.log((a = 3));
console.log((a += 3));
console.log((a -= 3));
console.log((a *= 3));
console.log((a /= 3));
console.log((a %= 3));

//Comparison Operator
// == === != !== > < <= >=

console.log(a == 3);
console.log(a === 3);
console.log(a != 3);
console.log(a !== 3);
console.log(a > 3);
console.log(a < 3);
console.log(a <= 3);
console.log(a >= 3);
